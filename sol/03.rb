map = DATA.read.strip.split

def count_trees map, dx, dy
  (0...map.size).step(dy).count do |y|
    x = ((y / dy) * dx) % map.first.size

    map[y][x] == "#"
  end
end

puts count_trees(map, 3, 1) # 205

product = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]].map do |dx, dy|
  count_trees map, dx, dy
end.inject(:*)

